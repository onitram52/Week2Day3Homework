def ravioli_ravioli():
    length = float(input("What is the length of you house in feet?: "))
    width = float(input("What is the width of your house in feet?: "))
    print("Your house's area is: ", (length * width), "square feet.")


from math import pi

def formuoli():
    radius = float(input("What is the radius of this circle in inches?: "))
    print("The circumference of this circle is: ", (2 * pi * radius), "inches.")
